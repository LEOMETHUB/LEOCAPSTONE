package org.sportmanagement.pojo;

public class TeamPojo {

    private String teamName;

    public TeamPojo(String teamName) {
        this.teamName = teamName;
    }

    public TeamPojo() {
    }

    public String getTeamName() {
        return teamName;
    }

    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }
}
