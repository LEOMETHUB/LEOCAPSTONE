package sporteventmanagement.pojo;

public class FieldPojo {

    private  String fieldNamePojo;
    private  String fieldAddressPojo;
    private  int fieldCapacityPojo;

    public String getFieldNamePojo() {
        return fieldNamePojo;
    }

    public void setFieldNamePojo(String fieldNamePojo) {
        this.fieldNamePojo = fieldNamePojo;
    }

    public String getFieldAddressPojo() {
        return fieldAddressPojo;
    }

    public void setFieldAddressPojo(String fieldAddressPojo) {
        this.fieldAddressPojo = fieldAddressPojo;
    }

    public int getFieldCapacityPojo() {
        return fieldCapacityPojo;
    }

    public void setFieldCapacityPojo(int fieldCapacityPojo) {
        this.fieldCapacityPojo = fieldCapacityPojo;
    }
}
